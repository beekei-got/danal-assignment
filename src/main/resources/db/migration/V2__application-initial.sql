CREATE TABLE normal_restaurant  (
    number BIGINT NOT NULL COMMENT '번호',
    service_name VARCHAR(50) DEFAULT NULL COMMENT '개방서비스명',
    service_id VARCHAR(50) DEFAULT NULL COMMENT '개방서비스아이디',
    government_code VARCHAR(50) DEFAULT NULL COMMENT '개방자치단체코드',
    management_number VARCHAR(50) DEFAULT NULL COMMENT '관리번호',
    permission_date VARCHAR(30) DEFAULT NULL COMMENT '인허가일자',
    permission_cancel_date VARCHAR(30) DEFAULT NULL COMMENT '인허가취소일자',
    open_status_code VARCHAR(50) DEFAULT NULL COMMENT '영업상태구분코드',
    open_status_name VARCHAR(50) DEFAULT NULL COMMENT '영업상태명',
    detail_open_status_code VARCHAR(50) DEFAULT NULL COMMENT '상세영업상태코드',
    detail_open_status_name VARCHAR(50) DEFAULT NULL COMMENT '상세영업상태명',
    closed_date VARCHAR(30) DEFAULT NULL COMMENT '폐업일자',
    closing_start_date VARCHAR(30) DEFAULT NULL COMMENT '휴업시작일자',
    closing_end_date VARCHAR(30) DEFAULT NULL COMMENT '휴업종료일자',
    re_open_date VARCHAR(30) DEFAULT NULL COMMENT '재개업일자',
    location_tel VARCHAR(50) DEFAULT NULL COMMENT '소재지전화',
    location_area_size VARCHAR(30) DEFAULT NULL COMMENT '소재지면적',
    location_zip_code VARCHAR(10) DEFAULT NULL COMMENT '소재지우편번호',
    location_address VARCHAR(200) DEFAULT NULL COMMENT '소재지전체주소',
    road_address VARCHAR(200) DEFAULT NULL COMMENT '도로명전체주소',
    road_zip_code VARCHAR(10) DEFAULT NULL COMMENT '도로명우편번호',
    business_name VARCHAR(100) DEFAULT NULL COMMENT '사업장명',
    last_updated_date VARCHAR(30) DEFAULT NULL COMMENT '최종수정시점',
    data_updated_type VARCHAR(50) DEFAULT NULL COMMENT '데이터갱신구분',
    data_updated_date VARCHAR(50) DEFAULT NULL COMMENT '데이터갱신일자',
    business_type VARCHAR(50) DEFAULT NULL COMMENT '업태구분명',
    coordinate_x VARCHAR(30) DEFAULT NULL COMMENT '좌표정보(x)',
    coordinate_y VARCHAR(30) DEFAULT NULL COMMENT '좌표정보(y)',
    hygiene_type VARCHAR(50) DEFAULT NULL COMMENT '위생업태명',
    male_worker_count INT DEFAULT NULL COMMENT '남성종사자수',
    female_worker_count INT DEFAULT NULL COMMENT '여성종사자수',
    circumference_type VARCHAR(50) DEFAULT NULL COMMENT '영업장주변구분명',
    grade_type VARCHAR(50) DEFAULT NULL COMMENT '등급구분명',
    water_supply_type VARCHAR(50) DEFAULT NULL COMMENT '급수시설구분명',
    total_worker_count INT DEFAULT NULL COMMENT '총직원수',
    office_worker_count INT DEFAULT NULL COMMENT '본사직원수',
    factory_desk_worker_count INT DEFAULT NULL COMMENT '공장사무직직원수',
    factory_sales_worker_count INT DEFAULT NULL COMMENT '공장판매직직원수',
    factory_producing_worker_count INT DEFAULT NULL COMMENT '공장생산직직원수',
    building_own_type VARCHAR(50) DEFAULT NULL COMMENT '건물소유구분명',
    guaranteed_amount DECIMAL(64,0) DEFAULT NULL COMMENT '보증액',
    monthly_rent_amount DECIMAL(64,0) DEFAULT NULL COMMENT '월세액',
    is_multiple_used VARCHAR(50) DEFAULT NULL COMMENT '다중이용업소여부',
    facility_size VARCHAR(30) DEFAULT NULL COMMENT '시설총규모',
    traditional_number VARCHAR(50) DEFAULT NULL COMMENT '전통업소지정번호',
    traditional_food VARCHAR(50) DEFAULT NULL COMMENT '전통업소주된음식',
    homepage VARCHAR(200) DEFAULT NULL COMMENT '홈페이지',
    created_datetime DATETIME(6) NOT NULL COMMENT '생성일자'
);
